function J = Jacobian(A,B,C,D,s,t)
J(1,1) = (1/4)*((A(1,1)+D(1,1)-C(1,1)-B(1,1))+(A(1,1)-B(1,1)-D(1,1)+C(1,1))*t);
J(2,1) = (1/4)*((A(1,1)+B(1,1)-C(1,1)-D(1,1))+(A(1,1)-B(1,1)-D(1,1)+C(1,1))*s);
J(1,2) = (1/4)*((A(1,2)+D(1,2)-C(1,2)-B(1,2))+(A(1,2)-B(1,2)-D(1,2)+C(1,2))*t);
J(2,2) = (1/4)*((A(1,2)+B(1,2)-C(1,2)-D(1,2))+(A(1,2)-B(1,2)-D(1,2)+C(1,2))*s);
end